package com.bean;
//用户实体类 hahahahah fdsaf
public class User {
	private int id; //编号
	private String name; //用户名
	private String pass; //密码
	
	//get set
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	
}
